def _build_category_breakdown(results):
    cats: dict[str, dict] = {}
    for r in results:
        c = r["category"]
        cats.setdefault(c, {"category": c, "total": 0, "found": 0,
                            "with_ingredients": 0, "with_regulatory": 0, "with_insights": 0})
        cats[c]["total"] += 1
        if r["found"]:              cats[c]["found"] += 1
        if r["has_ingredients"]:    cats[c]["with_ingredients"] += 1
        if r["has_regulatory"]:     cats[c]["with_regulatory"] += 1
        if r["has_insights"]:       cats[c]["with_insights"] += 1
    for v in cats.values():
        t = v["total"] or 1
        v["coverage_pct"]    = round(v["found"] / t * 100, 1)
        v["ingredient_pct"]  = round(v["with_ingredients"] / t * 100, 1)
        v["regulatory_pct"]  = round(v["with_regulatory"] / t * 100, 1)
        v["insights_pct"]    = round(v["with_insights"] / t * 100, 1)
    return list(cats.values())


def _build_region_breakdown(results):
    rgns: dict[str, dict] = {}
    for r in results:
        rg = r["region"]
        rgns.setdefault(rg, {"region": rg, "total": 0, "found": 0})
        rgns[rg]["total"] += 1
        if r["found"]: rgns[rg]["found"] += 1
    for v in rgns.values():
        v["coverage_pct"] = round(v["found"] / (v["total"] or 1) * 100, 1)
    return list(rgns.values())


