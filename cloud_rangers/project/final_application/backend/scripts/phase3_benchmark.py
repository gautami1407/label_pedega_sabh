# ─── Analysis helpers ─────────────────────────────────────────────────────────

def _analyse(barcode: str) -> tuple[dict, float]:
    """Call analyzer.analyze_barcode, return (result, elapsed_seconds)."""
    t0 = time.perf_counter()
    try:
        result = analyzer.analyze_barcode(barcode, user_preferences={})
        return result, time.perf_counter() - t0
    except Exception as exc:
        return {"_error": str(exc)}, time.perf_counter() - t0


def _has_real_ingredients(resp: dict) -> bool:
    ing = (resp.get("ingredients") or "").strip()
    return bool(ing) and ing.lower() not in ("not available", "n/a", "none", "")


def _has_regulatory_data(resp: dict) -> bool:
    reg = resp.get("regulatory") or {}
    csv_status = reg.get("csv_global_status") or []
    return isinstance(csv_status, list) and len(csv_status) > 0


def _has_regulatory_flags(resp: dict) -> bool:
    """Returns True if any regulated additive was flagged (banned/restricted)."""
    reg = resp.get("regulatory") or {}
    csv_status = reg.get("csv_global_status") or []
    return any(s.get("status") in ("Banned", "Restricted") for s in csv_status)


def _regulatory_flag_count(resp: dict) -> int:
    reg = resp.get("regulatory") or {}
    csv_status = reg.get("csv_global_status") or []
    return sum(1 for s in csv_status if s.get("status") in ("Banned", "Restricted"))


def _has_dashboard_insights(resp: dict) -> bool:
    return isinstance(resp.get("dashboard_insights"), dict)


def _detect_gemini_used(resp: dict) -> bool:
    """
    Heuristic: Gemini was used if dashboard_insights has:
    - healthier_alternatives with >=1 entry that has a non-generic description, OR
    - ingredient_purpose with >=3 entries where 'purpose' field is non-generic
    """
    di = resp.get("dashboard_insights") or {}
    if not di:
        return False
    alts = di.get("healthier_alternatives") or []
    ing = di.get("ingredient_purpose") or []
    # Fallback generates exactly 3 generic alternatives with known descriptions
    fallback_alt_descs = {
        "opt for whole grain alternatives with less processing",
        "organic versions use fewer synthetic additives",
        "making it at home gives full control over ingredients",
    }
    real_alts = [
        a for a in alts
        if a.get("description", "").lower() not in fallback_alt_descs
    ]
    # Fallback ingredient purpose is "Standard ingredient component"
    non_generic_ing = [
        i for i in ing
        if "standard ingredient component" not in i.get("purpose", "").lower()
    ]
    return len(real_alts) >= 1 or len(non_generic_ing) >= 3


def _detect_fallback_used(resp: dict) -> bool:
    """Fallback: all ingredient_purpose entries are generic OR no insights."""
    di = resp.get("dashboard_insights") or {}
    if not di:
        return True
    ing = di.get("ingredient_purpose") or []
    if not ing:
        return True
    generic_count = sum(
        1 for i in ing
        if "standard ingredient component" in i.get("purpose", "").lower()
    )
    return generic_count == len(ing)


def _ingredient_normalization(resp: dict) -> str:
    ing = (resp.get("ingredients") or "").strip()
    if not ing or ing.lower() in ("not available", "n/a", ""):
        return "MISSING"
    parts = [p.strip() for p in ing.split(",") if p.strip()]
    if len(parts) >= 3:
        return "PRESENT"
    if len(parts) >= 1:
        return "MINIMAL"
    return "MISSING"


def _count_unknown_ingredients(resp: dict) -> int:
    di = resp.get("dashboard_insights") or {}
    ing_purpose = di.get("ingredient_purpose") or []
    return sum(1 for i in ing_purpose if not i.get("purpose") or i.get("risk_level") == "Unknown")


def _total_ingredients(resp: dict) -> int:
    ing = (resp.get("ingredients") or "").strip()
    if not ing or ing.lower() in ("not available", "n/a", ""):
        return 0
    return len([p for p in ing.split(",") if p.strip()])


def _has_personalized_warnings(resp: dict) -> bool:
    di = resp.get("dashboard_insights") or {}
    return isinstance(di.get("personal_warnings"), list)


def _has_news_recalls(resp: dict) -> bool:
    di = resp.get("dashboard_insights") or {}
    sf = di.get("six_factor_engine") or {}
    news = sf.get("verified_news_and_recalls") or {}
    if isinstance(news, dict):
        return bool(news.get("articles") or news.get("recalls") or news.get("alerts"))
    return False


def _has_source_attr(resp: dict) -> bool:
    name = (resp.get("name") or "").strip()
    return bool(name) and name.lower() not in ("unknown product", "unknown", "")


def _has_timestamp(resp: dict) -> bool:
    di = resp.get("dashboard_insights") or {}
    sf = di.get("six_factor_engine") or {}
    news = sf.get("verified_news_and_recalls") or {}
    if isinstance(news, dict):
        return bool(news.get("timestamp") or news.get("fetched_at"))
    return False


def _concern_score(resp: dict) -> int | None:
    di = resp.get("dashboard_insights") or {}
    v = di.get("concern_score")
    return int(v) if v is not None else None


def _classify_status(resp: dict, found: bool) -> str:
    if not found:
        return "MISSING"
    if resp.get("_error"):
        return "ERROR"
    di = resp.get("dashboard_insights")
    if not di:
        return "PLACEHOLDER"
    if _detect_gemini_used(resp):
        return "WORKING"
    if _detect_fallback_used(resp):
        return "PARTIALLY_WORKING"
    return "MOCKED"


def _data_source(resp: dict, found: bool) -> str:
    if not found:
        return "NOT_FOUND"
    # Image URL present → came from OFF
    if resp.get("image"):
        return "OFF"
    # No image but has USDA-style nutrition list
    nutrition = resp.get("nutrition")
    if isinstance(nutrition, list):
        return "USDA"
    if isinstance(nutrition, dict) and nutrition:
        return "OFF"
    return "UNKNOWN"


# ─── Main Benchmark ───────────────────────────────────────────────────────────

def run_benchmark():
    ts_start = datetime.now(timezone.utc)
    print("\n" + "=" * 72)
    print("  LABEL PADEGHA SABH — Phase 3 Data Quality Benchmark")
    print(f"  Timestamp : {ts_start.isoformat()}")
    print(f"  Mode      : Direct (Analyzer bypass — no server required)")
    print(f"  Products  : {len(PRODUCTS)}")
    print("=" * 72 + "\n")

    results: list[dict] = []

    for idx, (barcode, label, category, region, hint) in enumerate(PRODUCTS, 1):
        print(f"[{idx:>3}/{len(PRODUCTS)}] {label:<48} {barcode}", end=" ... ", flush=True)

        resp, elapsed = _analyse(barcode)

        has_error = "_error" in resp
        found = not has_error and "error" not in resp

        # Core metrics
        has_ing      = _has_real_ingredients(resp) if found else False
        has_reg      = _has_regulatory_data(resp) if found else False
        has_di       = _has_dashboard_insights(resp) if found else False
        gemini_used  = _detect_gemini_used(resp) if found else False
        fallback     = _detect_fallback_used(resp) if found else False
        ing_norm     = _ingredient_normalization(resp) if found else "MISSING"
        unkn_ing     = _count_unknown_ingredients(resp) if found else 0
        total_ing    = _total_ingredients(resp) if found else 0
        unkn_ing_pct = round(unkn_ing / total_ing * 100, 1) if total_ing > 0 else 0.0
        reg_flags    = _regulatory_flag_count(resp) if found else 0
        has_warn     = _has_personalized_warnings(resp) if found else False
        has_news     = _has_news_recalls(resp) if found else False
        has_src      = _has_source_attr(resp) if found else False
        has_ts       = _has_timestamp(resp) if found else False
        score        = _concern_score(resp) if found else None
        status       = _classify_status(resp, found)
        src          = _data_source(resp, found)

        row = {
            "idx":                idx,
            "barcode":            barcode,
            "label":              label,
            "category":           category,
            "region":             region,
            "found":              found,
            "status":             status,
            "actual_name":        resp.get("name", "") if found else "",
            "data_source":        src,
            "has_ingredients":    has_ing,
            "ingredient_norm":    ing_norm,
            "unknown_ing_count":  unkn_ing,
            "total_ing_count":    total_ing,
            "unknown_ing_pct":    unkn_ing_pct,
            "has_regulatory":     has_reg,
            "regulatory_flagged": reg_flags,
            "has_insights":       has_di,
            "gemini_used":        gemini_used,
            "fallback_used":      fallback,
            "has_warnings":       has_warn,
            "has_news":           has_news,
            "has_source_attr":    has_src,
            "has_timestamp":      has_ts,
            "concern_score":      score,
            "elapsed_s":          round(elapsed, 2),
        }
        results.append(row)

        tag = "GEMINI" if gemini_used else ("FALLBACK" if fallback else ("ERR" if has_error else "?"))
        if not found:
            print("NOT FOUND")
        else:
            print(f"OK [{tag}] {elapsed:.1f}s | score={score} | reg_flags={reg_flags}")

        # Throttle Gemini to avoid rate limit
        if found and not has_error:
            time.sleep(GEMINI_SLEEP_S)

    # ─── Compute aggregate metrics ────────────────────────────────────────────
    total      = len(results)
    found_ct   = sum(1 for r in results if r["found"])
    not_fnd_ct = total - found_ct
    gemini_ct  = sum(1 for r in results if r["gemini_used"])
    fback_ct   = sum(1 for r in results if r["fallback_used"])
    has_ing_ct = sum(1 for r in results if r["has_ingredients"])
    has_reg_ct = sum(1 for r in results if r["has_regulatory"])
    has_di_ct  = sum(1 for r in results if r["has_insights"])
    has_warn_ct= sum(1 for r in results if r["has_warnings"])
    has_src_ct = sum(1 for r in results if r["has_source_attr"])
    has_ts_ct  = sum(1 for r in results if r["has_timestamp"])
